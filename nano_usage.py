# nano_usage.py - basic nano command and shortcut examples

# 1) Open or create a file in the terminal
# $ nano file.txt
# $ nano test.py

# 2) Open a system config file with sudo
# $ sudo nano /etc/hosts
# $ sudo nano /etc/apt/sources.list

# 3) Save (write) the file inside nano
# Ctrl + O   # write out (save), then press Enter to confirm the filename

# 4) Exit nano
# Ctrl + X   # exit; nano will ask to save changes if needed

# 5) Show help
# Ctrl + G   # open nano's help screen

# 6) Move cursor quickly
# Ctrl + A   # move to beginning of the current line
# Ctrl + E   # move to end of the current line

# 7) Search text
# Ctrl + W   # start a search, then type the keyword and press Enter
# Ctrl + W   # press again to find the next match

# 8) Replace text
# Ctrl + \   # search and replace

# 9) Cut and paste lines
# Ctrl + K   # cut the current line (can press multiple times to cut several lines)
# Ctrl + U   # paste the last cut text

# 10) Go to a specific line
# Ctrl + _   # then type the line number and press Enter
